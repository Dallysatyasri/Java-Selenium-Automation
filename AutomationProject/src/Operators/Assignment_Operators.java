package Operators;

public class Assignment_Operators {

	public static void main(String[] args) {
		// condition? true:false  
		
		int a =10;
		
		int b = 4;
		
		int c = 5;
		
		int d= 5;
		
		System.out.println(a >=  c );// 10 >= 5, true
		
		System.out.println(c >= d);//10 >=4 , true
		
		
		System.out.println(a <= b );// 10 <= 4, false
		
		System.out.println(d  >=  c );//  5 <= 5 , true
		
		
		

	}

}
