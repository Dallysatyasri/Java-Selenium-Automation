package Operators;

public class Relational_Operators {

	public static void main(String[] args) {
		// condition? true:false  
		
		int a =10;
		
		int b = 4;
		
		int c = 5;
		
		int d= 5;
		
		System.out.println(a > c );// 10 > 5, true
		
		System.out.println(c >= d);//4 > 5 , false
		
		
		System.out.println(a < b );// 10 < 4, false
		
		System.out.println(b < c );//  4< 5 , true
		
		System.out.println(b == c );//  4 == 5 , false
		
		System.out.println(d == c );//  5 == 5 , true
		
		
		
		
		

	}

}
