package Operators;

public class Ternary_Operators {

	public static void main(String[] args) {
		// condition? true:false  
		
		int a =10;
		
		
		
		System.out.println(a);// 10
		
		System.out.println("*******Pre-Incremental Operators*********");
		
		System.out.println(++a);// 1+10=11
		
		System.out.println(a);//11
		
		System.out.println(++a);//1+11=12
		
		System.out.println(a);//12
		
		System.out.println("*******Post-Incremental Operators*********");
		
		System.out.println(a);//12
		
		System.out.println(a++);//12+1=13
		
		System.out.println(a);//13
		
		System.out.println(a++);//13+1=14
		
		System.out.println(a);//14
		
		
		
		
		

		
		System.out.println("*******Pre-Decremental Operators*********");
		
		System.out.println(--a);// 1-14=13
		
		System.out.println(a);//13
		
		System.out.println(--a);//1-13=12
		
		System.out.println(a);//12	
		
System.out.println("*******Post-Decremental Operators*********");
		
		System.out.println(a);//12
		
		System.out.println(a--);//12-1=11
		
		System.out.println(a);//11
		
		System.out.println(a--);//11-1=10
		
		System.out.println(a);//10
		
		
		
		
		
		
		


		
		
		
		

	}

}
