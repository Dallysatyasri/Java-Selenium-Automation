package PrimitiveDatatypes;

public class Float_Datatype {

	public static void main(String[] args) {
		
		// comment single line of code
		
		
		
		
		/* 
		 * 
		 * Syntax: datatype varaiablename=value;
		*/
	
	// Declaration
		
		float num;
		
	// Intialization
		
		float number = 100;
		System.out.println(number); // 100.0
		
		number=3289;
		System.out.println(number); // 3289.0
		
		number=12300;
		System.out.println(number); // 12300.0
		
		number=-32738;
		System.out.println(number); // -32738.0
		
		number=78.98f;
		System.out.println(number); // 78.98f
		
		number=345.78f;
		System.out.println(number); // 345.78f
		
		
		
		
		

		
		


		
		
		 

	}

}
