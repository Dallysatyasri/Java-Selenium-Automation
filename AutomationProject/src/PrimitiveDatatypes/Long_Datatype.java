package PrimitiveDatatypes;

public class Long_Datatype {

	public static void main(String[] args) {
		
		// comment single line of code
		
		
		
		/* 
		 * 
		 * Syntax: datatype varaiablename=value;
		 */
	
	// Declaration
		
		long num;
		
	// Intialization
		
		long number = 100;
		System.out.println(number); // 100
		
		number=3289;
		System.out.println(number); // 3289
		
		number=12300;
		System.out.println(number); // 12300
		
		number=-32738;
		System.out.println(number); // -32738
		
		number=-1230085965L;
		System.out.println(number); // 12300
		
		number=-1273845678134567892L;
		System.out.println(number); // -32738
		
		
		

		
		


		
		
		 

	}

}
